
def pizza_making(size, *toppings):
    print(f"\nMaking a {size} -inch pizza with the following toppings: ")
    for topping in toppings:
        print(f"-{topping}")

def milkshake(size, *toppings):
    print(f"\n Making a {size} milkshake with the following toppings: ")
    for topping in toppings:
        print(f"-{topping}")

